#include "main.h"

char	*ft_remove_white_spaces(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] != ' ')
		{
			str[j] = str[i];
			j++;
		}		
		i++;
	}
	str[j] = '\0';
	return (str);
}
